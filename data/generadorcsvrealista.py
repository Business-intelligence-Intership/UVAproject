def generar_matricula(año, i):
    return f"{año}{i:05d}"

def seleccionar_escuela(año):
    lista = escuelas_por_anio[año]
    pesos = []

    for escuela in lista:
        if "COBAY" in escuela or "CBTIS" in escuela or "CONALEP" in escuela:
            pesos.append(6)
        elif "PREPARATORIA" in escuela or "COLEGIO" in escuela:
            pesos.append(3)
        else:
            pesos.append(1)

    pesos = np.array(pesos)
    pesos = pesos / pesos.sum()

    return np.random.choice(lista, p=pesos)

def asignar_modalidad(año):
    p = {
        2018: (0.85, 0.15),
        2019: (0.80, 0.20),
        2020: (0.75, 0.25),
        2021: (0.70, 0.30),
        2022: (0.65, 0.35),
        2023: (0.60, 0.40)
    }[año]
    return np.random.choice(["Escolar", "Mixto"], p=p)

def asignar_sexo(carrera):
    if "Ing" in carrera:
        return np.random.choice(["Hombre", "Mujer"], p=[0.7, 0.3])
    elif carrera in ["Psicología", "Enfermería"]:
        return np.random.choice(["Hombre", "Mujer"], p=[0.3, 0.7])
    else:
        return np.random.choice(["Hombre", "Mujer"], p=[0.5, 0.5])

def generar_fecha_nacimiento(modalidad):
    if modalidad == "Escolar":
        edad = int(np.random.normal(20, 2))
    else:
        edad = int(np.random.normal(28, 6))

    edad = max(17, min(55, edad))

    hoy = datetime.now()
    nacimiento = hoy - timedelta(days=edad*365 + random.randint(0,365))
    return nacimiento.date(), edad

def asignar_nacionalidad():
    return np.random.choice(["Mexicana", "Extranjera"], p=[0.95, 0.05])

def asignar_lugar(nacionalidad):
    if nacionalidad == "Mexicana":
        return np.random.choice(
            ["Yucatán", "Campeche", "Quintana Roo", "Tabasco"],
            p=[0.7, 0.1, 0.1, 0.1]
        )
    else:
        return np.random.choice(["Colombia", "Perú", "Estados Unidos"])


def asignar_turno(modalidad):
    if modalidad == "Escolar":
        return np.random.choice(["Matutino", "Vespertino"])
    else:
        return "Sabatino"

def asignar_ubicacion_escuela(escuela):
    if "COBAY" in escuela or "CBTIS" in escuela:
        return "Municipio"
    elif "UADY" in escuela:
        return "Merida"
    else:
        return np.random.choice(["Merida", "Foraneo"], p=[0.7, 0.3])

def generar_ciclo(anio, carrera):
    if carrera == "Odontología":
        ciclo = np.random.choice(
            CICLO_SEMESTRAL,
            p=[0.65, 0.35]
        )
    else:
        ciclo = np.random.choice(
            CICLO_CUATRIMESTRAL,
            p=[0.5, 0.3, 0.2]
        )
    
    return f"{ciclo} {anio}"

