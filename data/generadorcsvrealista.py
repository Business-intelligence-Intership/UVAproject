def generar_datos(año, total):
    data = []

    for i in range(total):
        matricula = generar_matricula(año, i)

        carrera = np.random.choice(carreras_keys, p=carreras_pesos)
        modalidad = asignar_modalidad(año)
        sexo = asignar_sexo(carrera)

        fecha_nac, edad = generar_fecha_nacimiento(modalidad)

        nacionalidad = asignar_nacionalidad()
        lugar = asignar_lugar(nacionalidad)


        escuela = seleccionar_escuela(año)
        ubicacion_escuela = asignar_ubicacion_escuela(escuela)

        colonia = random.choice(colonias)

        turno = asignar_turno(modalidad)

        data.append([
            matricula, sexo, nacionalidad, lugar, fecha_nac,
            colonia, escuela, modalidad,
            ubicacion_escuela, "Licenciatura", carrera,
            turno, generar_ciclo(año, carrera)
        ])

    return pd.DataFrame(data, columns=[
        "matricula","sexo","nacionalidad","lugar_nacimiento","fecha_nacimiento",
        "colonia","escuela_procedencia","modalidad",
        "ubicacion_escuela_procedencia","nivel","carrera","turno","ciclo"
    ])

